#include "libft.h"

void	*ft_memset(void *str, int a, size_t num)
{
	char	b;
	int		i;

	i = 0;
	b = a;
	while (num != 0)
	{
		((char *)str)[i] = b;
		i++;
		num--;
	}
	return (str);
}
